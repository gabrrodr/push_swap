#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != (unsigned char)c)
	{
		if (!s[i])
		{
			return (NULL);
		}
		i++;
	}
	return ((char *)s + i);
}
